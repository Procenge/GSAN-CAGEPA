package gcom.gui.cadastro.empresa;

import gcom.cadastro.empresa.Empresa;
import gcom.fachada.Fachada;
import gcom.gui.GcomAction;
import gcom.gui.ManutencaoRegistroActionForm;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.apache.struts.action.ActionForm;
import org.apache.struts.action.ActionForward;
import org.apache.struts.action.ActionMapping;

/**
 * @author Virgínia Melo
 */
public class RemoverEmpresaAction
				extends GcomAction {

	/**
	 * Este caso de uso permite remover uma ou mais Empresas.
	 * 
	 * @author Virgínia Melo
	 * @date 07/08/2008
	 */

	public ActionForward execute(ActionMapping actionMapping, ActionForm actionForm, HttpServletRequest httpServletRequest,
					HttpServletResponse httpServletResponse){

		// Seta o retorno
		ActionForward retorno = actionMapping.findForward("telaSucesso");

		// Obtém a instância da fachada
		Fachada fachada = Fachada.getInstancia();

		ManutencaoRegistroActionForm manutencaoRegistroActionForm = (ManutencaoRegistroActionForm) actionForm;

		String[] idsRegistrosRemocao = manutencaoRegistroActionForm.getIdRegistrosRemocao();

		fachada.remover(idsRegistrosRemocao, Empresa.class.getName(), null, null);

		montarPaginaSucesso(httpServletRequest, idsRegistrosRemocao.length + " Empresa(s) excluída(s) com sucesso.",
						"Manter outra Empresa", "exibirFiltrarEmpresaAction.do?menu=sim");

		return retorno;

	}

}
